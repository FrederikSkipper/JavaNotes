package logic;

public class PrimitiveAndReferenceTypes {
    public static void main(String[] args) {
        
    /* Primitive types */
    int a = 10; // Value of a can not be changed. (They are immutable).
    System.out.println(a); // prints a
    
    /* Reference types */
    String b = "huh"; // Strings are also immutable. 
    // All objects require a toString method to be printed. 
    }
}
