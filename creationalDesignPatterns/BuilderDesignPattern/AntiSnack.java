package creationalDesignPatterns.BuilderDesignPattern;

public class AntiSnack {
    private String name;
    private int size;
    private int price;
    private String catagory;

public AntiSnack(String name, int size, int price, String catagory) {
    this.name = name;
    this.size = size;
    this.price = price;
    this.catagory = catagory;
    }
}